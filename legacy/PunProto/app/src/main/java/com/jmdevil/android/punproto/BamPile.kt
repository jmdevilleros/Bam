package com.jmdevil.android.punproto

// ----------------------------------------------------------------------------------------
import cards.*

// ----------------------------------------------------------------------------------------
class BamPile(name: String, val suit: Card.Suit) : CardStack(name) {
    // ------------------------------------------------------------------------------------
    fun next() = when {
        isFull()  -> null
        isEmpty() -> Card(Card.Rank.ACE, suit)
        else      -> Card(top()!!.rank.nextOrNull()!!, suit)
    }
    // ------------------------------------------------------------------------------------
    fun isFull() = top()?.rank == Card.Rank.KING
}